// Operadores aritméticos

//multiplicação *
console.log(3.14 * 2);

//divisão /
console.log(12 / 5);


//soma +
console.log(1.61 + 5);

//subtração -
console.log(2 - 2);

//resto da divisão %
console.log(13 % 3);

//incremento ++
let increment = 0;
increment++;
console.log(increment);

//decremento --
let decrement = 0;
console.log(--decrement);

//exponencial ** or Math.pow(b,e)
console.log(2 ** 3);
console.log(Math.pow(27, 1/3))