/**
    Javascript é um linguaem de tipagem fraca e dinâmica
      * tipagem fraca - Ao declarar uma variável, não temos que especificar o tipo 
                        de dado que será recebido pela raviável;
      * dinâmica - O tipo da variável é determinado conforme o tipo 
                   do dado passado para a variável;

 */

 let clima = true // aqui é boolean
 console.log(typeof(clima))

 clima = "" // aqui já passa a ser string
 console.log(typeof(clima))

 /*
    A variável só não poderá mudar de tipo, bem como o seu valor, 
    caso seja uma constante (const).
 */